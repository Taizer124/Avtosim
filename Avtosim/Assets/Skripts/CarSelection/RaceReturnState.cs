using UnityEngine;

namespace Assets.VehicleController
{
    /// <summary>
    /// Мостик между RaceStartZone и RaceFinishZone. При старте гонки старт-зона
    /// выключает именно ту машину, на которой игрок реально приехал к линии
    /// старта (выбранную в меню, стоящую на месте старта) и кладёт её сюда.
    /// На финише финиш-зона забирает её отсюда и включает обратно — так после
    /// гонки восстанавливается ИМЕННО выбранное авто и ИМЕННО в точке старта,
    /// а не фиксированный _oldVehicle у точки респавна уровня.
    ///
    /// Статик, потому что старт- и финиш-зоны — разные объекты сцены без прямой
    /// ссылки друг на друга, а жизнь значения — в пределах одного заезда.
    /// </summary>
    public static class RaceReturnState
    {
        /// <summary>
        /// Машина, которую надо восстановить после гонки (та, что въехала в
        /// старт-зону). null, если старт-зона её не захватила — тогда финиш
        /// откатывается на свой _oldVehicle.
        /// </summary>
        public static GameObject ArrivalVehicle;

        /// <summary>Сбросить после восстановления, чтобы следующий заезд не подхватил устаревшую ссылку.</summary>
        public static void Clear() => ArrivalVehicle = null;
    }
}
